#gpt-coding{入力された西暦年がオリンピックの開催年か判定する。開催年の場合、夏か冬かも判定する またロジックはできるだけシンプルにし、開催年のリストを作らないでください}

# 入力された西暦年を取得
year = int(input("西暦年を入力してください: "))

# 夏季オリンピック開催年の判定
if (year - 1896) % 4 == 0 and year >= 1896:
    # 西暦1896年以降で4年ごとに夏季オリンピックが開催されている
    print(str(year) + "年は夏季オリンピックが開催された年です。")
    # 冬季オリンピック開催年の判定
    if year % 4 == 0 and year % 100 != 0 or year % 400 == 0:
        # 夏季オリンピックが開催された年であり、かつ西暦年が4で割り切れる場合は、冬季オリンピックが開催された年でもある
        print(str(year) + "年は夏季・冬季オリンピックが開催された年です。")
    else:
        # 夏季オリンピックが開催された年であり、かつ西暦年が4で割り切れない場合は、冬季オリンピックは開催されなかった
        print(str(year) + "年は夏季オリンピックが開催された年であり、冬季オリンピックは開催されませんでした。")
else:
    # 西暦年が夏季オリンピック開始年以前の場合は、夏季オリンピックは開催されていない
    print(str(year) + "年は夏季オリンピックが開催されていない年です。")





