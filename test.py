#gpt-coding{入力された西暦年がオリンピックの開催年か判定する。開催年の場合、夏か冬かも判定する}

# 入力を受け取る
year = int(input("西暦年を入力してください: "))

# 夏季オリンピックは4年おきに開催されるため、4で割り切れる年は開催年である
if year % 4 == 0:
    # 冬季オリンピックは夏季オリンピックの中間年に開催されるため、100で割り切れる年は除外される
    if year % 100 != 0:
        print(str(year) + "年は夏季オリンピックの開催年です。")
    # ただし、400で割り切れる年は例外的に開催されるため、除外されない
    elif year % 400 == 0:
        print(str(year) + "年は夏季オリンピックの開催年です。")
    else:
        print(str(year) + "年はオリンピックの開催年ではありません。")
# それ以外の年は開催年ではない
else:
    print(str(year) + "年はオリンピックの開催年ではありません。")



